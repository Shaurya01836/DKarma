export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-4 py-2 rounded-md text-sm font-medium inline-block text-center font-display transition";

  const variantStyles = {
    primary: "bg-primary text-white hover:bg-primary-hover",
    secondary:
      "bg-transparent border border-border text-foreground hover:bg-muted",
    dark: "bg-foreground text-background hover:bg-muted",
    gradient: "bg-gradient-to-b from-primary to-primary-hover text-white",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};
